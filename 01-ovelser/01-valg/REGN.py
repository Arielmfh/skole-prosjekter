# Vi dekalreer (oppretter) variabler og gir startverdier

tall_1 =  15
tall_2 = 3
svar = 0

# Vi ser på de vanligste regneoperasjonene
#Addisjon (pluss)
svar = tall_1 + tall_2
print(tall_1,"+",tall_2,"=",svar)

# Substraksjon (minus)
svar = tall_1 - tall_2
print(tall_1,"-",tall_2,"=",svar)

# Multiplikasjon (gange)
svar = tall_1 * tall_2
print(tall_1,"x",tall_2,"=",svar)

# Divisjon (dele)
svar = tall_1 / tall_2
print(tall_1,"/",tall_2,"=",svar)

# Potens (opphøy i)
svar = tall_1 ** tall_2
print(tall_1,"opphøyd i",tall_2,"=",svar)

# Inkrementell vekst
print(tall_1,"skal økes med 1")
tall_1 = tall_1 + 1
print(tall_1,"er nå økt med 1")
tall_1 += 1
print(tall_1,"er nå økt med 1")